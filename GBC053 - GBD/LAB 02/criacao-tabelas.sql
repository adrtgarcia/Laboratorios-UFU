    -- feminino
    else
        tid := trunc(random() * 25 + 1);
        select nome into temp from vnomef where id = tid;
        retorno := temp || chr(32);
        nomedomeio := trunc(random() * 2 + 1);
        if (nomedomeio = 1) then
           tid := trunc(random() * 25 + 1);
           select nome into temp from vnomef where id = tid;
           retorno := retorno || chr(32) || temp;
        end if;
    end if;
    -- sobrenome
    tid := trunc(random() * 41 + 1);
    select sobrenome into temp from vsobrenome where id = tid;
    retorno := retorno || chr(32) || temp;
    segundosobrenome := trunc(random() * 2 + 1);

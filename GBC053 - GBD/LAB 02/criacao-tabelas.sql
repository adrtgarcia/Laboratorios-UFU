create or replace function gera_data_pedido()
returns date as $$
declare
    maiordata date;
    dataretorno date;
    maxpedidos integer;
    qpedidos integer;
BEGIN
    maxpedidos := trunc(random() * 5000 + 1) + 1000;
    select max(data) into maiordata from pedido;
    if (maiordata is null) then
        maiordata := now();
    end if;
    select count(data) into qpedidos from pedido where data = maiordata;
    if (qpedidos >= maxpedidos) then
        dataretorno := maiordata + 1;
    else
        dataretorno := maiordata;
    end if;
    return dataretorno;
END
$$ language 'plpgsql';


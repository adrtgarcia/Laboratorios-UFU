create or replace function calcula_cliente_sexo(in nomecompleto char(100))
returns char(1) as $$
declare
    primeiro_nome char(20);
    quant integer;
BEGIN
    select substring(nomecompleto from 1 for position(' ' in nomecompleto)-1) into primeiro_nome;
    select count(*) into quant from vnomem where nome = primeiro_nome;
    if (quant = 1) then
        return 'M';
    end if;
    select count(*) into quant from vnomef where nome = primeiro_nome;
    if (quant = 1) then
        return 'F';
    end if;
    return 'I';
END
$$ language 'plpgsql';

create or replace function gera_nome_cliente()
returns varchar(50) as $$

create or replace function gera_pedidos(in quantidade integer)
returns void as $$
declare
    max_pk_cliente integer;
    max_pk_vendedor integer;
    max_pk_produto integer;
    ii float;
    qtde float;
	inicio timestamp;
	passo timestamp;
BEGIN
    select max(CODCLI) into max_pk_cliente from cliente;
    select max(CODVEND) into max_pk_vendedor from vendedor;
    select max(CHAVE_ALTERNATIVA) into max_pk_produto from PRODUTO;
    inicio := clock_timestamp();
	passo := clock_timestamp();
    raise notice 'Pedidos: inicio das insercoes as %', timeofday();
    FOR i IN 1..quantidade LOOP
        PERFORM gera_pedido(max_pk_cliente, max_pk_vendedor, max_pk_produto);
        if (i % 1000 = 0) then
            ii := i;
            qtde := quantidade;
            -- now(): Current date and time (start of current transaction)
            -- statement_timestamp(): Current date and time (start of current statement)
            -- timeofday(): Current date and time (like clock_timestamp, but as a text string)
            raise notice 'Pedidos: % inseridos (% % concluido) - parcial % - total %', i, ii/qtde*100.0, chr(37), age(clock_timestamp(),passo), age(clock_timestamp(),inicio);
			passo := clock_timestamp();
        end if;
    END LOOP;
    select count(*) into max_pk_cliente from cliente;
    select count(*) into max_pk_vendedor from pedido;
    select count(*) into max_pk_produto from itemped;
    raise notice '--------------------------------------';
    raise notice 'Resumo:';
    raise notice 'Clientes: % linhas', max_pk_cliente;
    raise notice 'Pedidos: % linhas', max_pk_vendedor;
    raise notice 'Itens de pedidos: % linhas', max_pk_produto;
    raise notice '--------------------------------------';
END
$$ language 'plpgsql';

